b -= 40 # b = 40 - 2
print(f"Resta de b: {b}")

c = 20
c *= 29 # c = 20 * 29
print(f"Multiplicación de c: {c}")

d = 2
d **= 3
print(f"Exponende te d: {d}")

e = 10
e //= 3
print(f"División de e: {e}")

f = 10
f %= 3
print(f"Modulo de f: {f}")


