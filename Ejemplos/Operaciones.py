#Operaciones matematicas
x = 68
y = 23

print(f"Operación de suma de {x} y {y} y como resultado da {x+y}")
print(f"Operación de resta de {x} y {y} y como resultado da {x-y}")
print(f"Operación de multiplicación de {x} y {y} y como resultado da {x*y}")
print(f"Operación de división de {x} y {y} y como resultado da {x/y}")

#División al piso
print(f"Operación de división al piso de {x} y {y} y como resultado da {x//y}")

print(f"Operación de modulo de {x} y {y} y como resultado da {x%y}")

print(f"Operación de elevado a la potencia de 3 de {x} y {y} y como resultado da {x**3}")
print(f"Operación de elevado a la potencia de 64, {x} y {y} y como resultado da {x**y}")
print(f"Operación de elevado a la potencia de 2 de {x} y {y} y como resultado da {x**2}")

print(f"Operación de raíz de {x} y {y} y como resultado da {x**0.5}")

#Redondeo

print(f"Operación de raíz de {x} y {y} y como resultado da {round(x**0.5, 2)}")
print(f"Operación de raíz de {x} y {y} y como resultado da {round(x**0.5)}")

